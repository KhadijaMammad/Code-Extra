// Arithmetic Operators
let x;
let y;
x = 6;
y = 5;
console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);
console.log(x%y);

// Assigment Operators
let num1 = 15;
let num2 = 3;

console.log(num1+=num2); 
console.log(num1-=num2); 
console.log(num1*=num2); 
console.log(num1/=num2); 
console.log(num1%=num2);

// Logical Operators 

let a = true;
let b = false;
console.log(a && b);
console.log(a || b);
console.log(!a);

let c = 5;
let d = 10;
console.log(c && d);
console.log(c || d);
console.log(!c);

// Comparison Operators
let e = 5;
let f = 10;
console.log(e > f);
console.log(e < f);
console.log(e >= f);
console.log(e <= f);








